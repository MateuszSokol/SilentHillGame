package ms.enemies;

import ms.Player;

public abstract class Enemy {
    private int Hp;
    private int Exp;

    public int getHp() {
        return Hp;
    }

    public void setHp(int hp) {
        Hp = hp;
    }

    public int getEXP() {
        return Exp;
    }

    public void setEXP(int exp) {
        Exp = exp;

    }





}
